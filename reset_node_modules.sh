#!/bin/bash
set -eu

echo "🗑  Xóa thư mục node_modules trong host..."
rm -rf ./node_modules ./package-lock.json || true

echo "🗑  Xóa docker volume node_modules..."
docker compose down
docker volume rm $(basename "$PWD")_node_modules || true

echo "🚀 Khởi động lại containers..."
docker compose up -d --build

echo "✅ Đã reset node_modules. Giờ bạn có thể exec vào container node và chạy:"
echo "   ./exec_node.sh npm install"
